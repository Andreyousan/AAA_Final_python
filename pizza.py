    def __init__(self, size: str = 'L'):
        """
        –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —ç–∫–∑–µ–º–ø–ª—è—Ä–∞, –∑–∞–¥–∞–µ—Ç—Å—è –∏–º—è, —Ä–∞–∑–º–µ—Ä –∏ –∏–Ω–≥—Ä–∏–¥–∏–µ–Ω—Ç—ã
        """
        self.name = '–û—Å–Ω–æ–≤–∞'
        self.size = size
        self.add_ingredients(
            ['—Ç–æ–º–∞—Ç–Ω—ã–π —Å–æ—É—Å', '–º–æ—Ü–∞—Ä–µ–ª–ª–∞'],
            ['ü•´', 'üßÄ']
        )

    def add_ingredients(self, keys: list[str], emojis: list[str]):
        """
        –î–æ–±–∞–≤–ª—è–µ—Ç –º–∞—Å—Å–∏–≤ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ —Å–ª–æ–≤–∞—Ä—å —Å —Ä–µ—Ü–µ–ø—Ç–æ–º.
        –ï—Å–ª–∏ –µ–≥–æ –Ω–µ—Ç, —Ç–∞–∫–∂–µ —Å–æ–∑–¥–∞–µ—Ç —Å–ª–æ–≤–∞—Ä—å.
        """
        if not hasattr(self, '_recipe'):
            self._recipe = dict()

        for key, emoji in zip(keys, emojis):
            self._recipe[key] = emoji

    def dict(self):
        """
        –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ä–µ—Ü–µ–ø—Ç –ø–∏—Ü—Ü—ã –≤ –≤–∏–¥–µ —Å–ª–æ–≤–∞—Ä—è.
        """
        return self._recipe

    def fancy_print(self):
        """
        –í—ã–≤–æ–¥ —Ä–µ—Ü–µ–ø—Ç–∞ —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º –ø–∏—Ü—Ü—ã –∏ —ç–º–æ–¥–∑–∏.
        """
        print(f'{self.name}:', end=' ')
        for key in self.dict().keys():
            print(f'{key}{self.dict()[key]}', end=' ')
        print()

    def __eq__(self, other):
        """
        –°—Ä–∞–≤–Ω–µ–Ω–∏–µ –¥–≤—É—Ö –ø–∏—Ü—Ü.
        –í—ã–≤–æ–¥–∏—Ç –æ—Ç–ª–∏—á–∏—è –∏ –æ–±—â–∏–µ –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç—ã –¥–≤—É—Ö –ø–∏—Ü—Ü.
        –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç True –ø—Ä–∏ –ø–æ–ª–Ω–æ–º —Å–æ–≤–ø–∞–¥–µ–Ω–∏–∏ –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç–æ–≤, –∏–Ω–∞—á–µ False.
        """
        similar = dict()
        unique_self = dict()
        unique_other = dict()

        for key in self.dict().keys():
            if key in other.dict().keys():
                similar[key] = self.dict()[key]
            else:
                unique_self[key] = self.dict()[key]

        for key in other.dict().keys():
            if key not in self.dict().keys():
                unique_other[key] = other.dict()[key]

        print('–°—Ö–æ–∂–∏–µ –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç—ã:', *similar.values())
        print(f'{self.name} —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ:', *unique_self.values())
        print(f'{other.name} —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ:', *unique_other.values())

        return self.dict() == other.dict()


